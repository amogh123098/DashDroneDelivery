    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int weight = Integer.parseInt(jTextField1.getText());
        String type = jTextField2.getText();
        String delivery = jTextField3.getText();
        int type_amount;
        int delivery_amount;
        int Total;
    switch (type) {
        case "Regular":
            type_amount = 5;
            break;
        case "Fragile":
            type_amount = 10;
            break;
        case "Waterproof":
            type_amount = 10;
            break;
        case "Gold":
            type_amount = 15;
            break;
        case "Premium":
            type_amount = 20;
            break;
        default:
            type_amount = 5;
            break;}
    
        switch (delivery) {
        case "Regular":
            delivery_amount = 5;
            break;
        case "Superfast":
            delivery_amount = 10;
            break;
        case "Premium":
            delivery_amount = 15;
            break;
        default:
            delivery_amount = 5;
            break;
        }
        Total = (type_amount + delivery_amount) * 20 + weight * 20;
        jTextField4.setText("" + Total + " Rupees");
    }//GEN-LAST:event_jButton4ActionPerformed

